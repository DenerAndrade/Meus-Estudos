# PILERES DA ORIENTAÇÃO A OBJETOS

#   -  Herança
#   -  polimorfismo
#   -  Encapsulamento
#   -  Abstração

# --------------------HERANÇA-------------------------
#  herdar comportamento a partir de outro objeto.
#  grande obetivo da herança é o reuso do código.

# ------------------ENCAPSULAMENTO--------------------
#  A capacidade de usar determinado objeto sem entender a parte interna do objeto.
#  o encapsulamento ajuda a garantir que um objeto seja manipulado de maneira controlada..
# ..sem expor seus detalhes internos, promovendo a segurança, a manutenção e a reutilização do código.

#--------------------POLIMORFISMO---------------------
# O polimorfismo em POO é a capacidade de usar a mesma interface para representar 
# diferentes tipos de dados ou comportamentos.


#-----------------------ABSTRAÇÃO---------------------
# à ideia de esconder os detalhes complexos de implementação e mostrar apenas as 
# informações essenciais para o usuário ou outro sistema. Em outras palavras, abstração permite 
# que você foque no que um objeto faz, e não em como ele faz.